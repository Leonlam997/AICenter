package com.leon.aicenter.bean;

public class HomeDeviceBean {
    private int type;
    private int id;

    public HomeDeviceBean() {
        this.type = 0;
        this.id = 0;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
